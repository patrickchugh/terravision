# ── Helpers ───────────────────────────────────────────────────────────


def _parse_model(xml_str):
    """Parse emitter output and return the mxGraphModel element."""
    mxfile = ET.fromstring(xml_str)
    return mxfile.find(".//mxGraphModel")


