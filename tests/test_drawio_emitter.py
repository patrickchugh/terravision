class TestEmitDrawioValidXML:
    def test_produces_valid_xml(self):
        xml = emit_drawio(_simple_graph(), {}, set(), {}, {})
        root = ET.fromstring(xml)
        assert root.tag == "mxfile"
        diagram = root.find("diagram")
        assert diagram is not None
        model = diagram.find("mxGraphModel")
        assert model is not None

    def test_root_cells_present(self):
        xml = emit_drawio(_simple_graph(), {}, set(), {}, {})
        root = _parse_model(xml)
        cells = root.findall(".//mxCell")
        ids = {c.get("id") for c in cells}
        assert "0" in ids
        assert "1" in ids

    def test_node_count_matches(self):
        g = _simple_graph()
        g.nodes["node2"] = XdotNode(
            id="node2", pos=(300, 300), width=1.0, height=1.0, label="node2"
        )
        xml = emit_drawio(g, {}, set(), {}, {})
        root = _parse_model(xml)
        vertices = [c for c in root.findall(".//mxCell") if c.get("vertex") == "1"]
        assert len(vertices) == 2  # Two resource nodes


class TestCoordinateTransformation:
    def test_y_flip(self):
        """Node at Graphviz y=300 in a graph height=400 should be at drawio y≈100."""
        g = XdotGraph(bounding_box=(0, 0, 500, 400))
        g.nodes["n"] = XdotNode(
            id="n", pos=(250, 300), width=1.0, height=1.0, label="n"
        )
        xml = emit_drawio(g, {}, set(), {}, {})
        root = _parse_model(xml)
        node_cell = [
            c
            for c in root.findall(".//mxCell")
            if c.get("vertex") == "1" and c.get("value") == "n"
        ]
        assert len(node_cell) == 1
        geo = node_cell[0].find("mxGeometry")
        y = float(geo.get("y"))
        # Graphviz y=300, graph_height=400, flipped = 400-300=100, minus half height
        assert y < 200  # Should be near top of canvas


class TestClusterRendering:
    def test_cluster_cell_created(self):
        xml = emit_drawio(_graph_with_cluster(), {}, set(), {}, {})
        root = _parse_model(xml)
        cells = root.findall(".//mxCell")
        cluster_cells = [
            c
            for c in cells
            if c.get("vertex") == "1" and "container=1" in (c.get("style") or "")
        ]
        assert len(cluster_cells) == 1

    def test_nested_cluster_parent(self):
        g = XdotGraph(bounding_box=(0, 0, 800, 600))
        g.clusters["cluster_outer"] = XdotCluster(
            name="cluster_outer", bb=(10, 10, 700, 500), label="Outer"
        )
        g.clusters["cluster_inner"] = XdotCluster(
            name="cluster_inner",
            bb=(50, 50, 400, 300),
            label="Inner",
            parent="cluster_outer",
        )
        xml = emit_drawio(g, {}, set(), {}, {})
        root = _parse_model(xml)
        cells = {c.get("value"): c for c in root.findall(".//mxCell")}
        inner = cells.get("Inner")
        outer = cells.get("Outer")
        assert inner is not None
        assert outer is not None
        assert inner.get("parent") == outer.get("id")


