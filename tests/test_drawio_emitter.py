class TestLoadShapeMap:
    def test_aws_map_loads(self):
        m = load_shape_map("aws")
        assert len(m) > 50
        assert "aws_lambda_function" in m

    def test_azure_map_loads(self):
        m = load_shape_map("azure")
        assert len(m) > 30

    def test_gcp_map_loads(self):
        m = load_shape_map("gcp")
        assert len(m) > 20

    def test_unknown_provider_returns_empty(self):
        m = load_shape_map("nonexistent_provider")
        assert m == {}

    def test_shape_names_follow_convention(self):
        """Shape names must use draw.io conventions per provider."""
        for provider in ("aws", "azure", "gcp"):
            m = load_shape_map(provider)
            for resource_type, shape_name in m.items():
                valid = shape_name.startswith("mxgraph.") or shape_name.startswith(
                    "img/lib/"
                )
                assert valid, (
                    f"{provider}/{resource_type}: {shape_name} "
                    "doesn't start with mxgraph. or img/lib/"
                )

    def test_no_duplicate_mappings(self):
        """No two resource types should map to the same shape."""
        for provider in ("aws", "azure", "gcp"):
            m = load_shape_map(provider)
            values = list(m.values())
            # Allow duplicates for variants (e.g., aws_alb and aws_lb_alb can map to same shape)
            # Just check there's no identical key
            assert len(m.keys()) == len(set(m.keys()))


